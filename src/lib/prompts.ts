// Helper functions
export function getRandomPrompt(): string {
  return RANDOM_PROMPTS[Math.floor(Math.random() * RANDOM_PROMPTS.length)];
}

export function getRandomAppend(): string {
  return RANDOM_APPENDS[Math.floor(Math.random() * RANDOM_APPENDS.length)];
}

// Process prompt variables like {option1|option2}
export function processPromptVariables(prompt: string): string {
  return prompt.replace(/\{([^}]+)\}/g, (match, content) => {
    const options = content.split('|');
    // Handle weighted options like "option:10|option:3"
    const weightedOptions: string[] = [];
    options.forEach((opt: string) => {
      const parts = opt.trim().split(':');
      const weight = parts.length > 1 ? parseInt(parts[1]) : 1;
      for (let i = 0; i < weight; i++) {
        weightedOptions.push(parts[0].trim());
      }
    });
    return weightedOptions[Math.floor(Math.random() * weightedOptions.length)];
  });
}

// Generate a complete random prompt with append
export function generateRandomPrompt(): string {
  const base = getRandomPrompt();
  const append = Math.random() > 0.7 ? `, ${getRandomAppend()}` : '';
  return processPromptVariables(base) + append;
}
